/* Operador Ternário */

// Quando precisar utilizar uma estrutura condicional onde só é realizado um tipo de teste de condição, podemos diminuir o nosso código e deixá-lo mais limpo com a utilização do operador ternário do JavaScript.

console.log(2 + 2 == 4 ? "sim" : "não")

/* Onde primeiro passamos a nossa condição, depois um ponto de interrogação e por último os retornos caso a condição seja verdadeira ou falsa, separadas por dois pontos.

Também é possível armazenar o resultado em variáveis */

